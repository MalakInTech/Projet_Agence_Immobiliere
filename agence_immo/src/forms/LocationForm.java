    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
        // TODO add your handling code here:
       
   try {
    if (selectedId == -1) {
        JOptionPane.showMessageDialog(this, "Sélectionnez une location !");
        return;
    }

    Location l = new Location();
    l.setId(selectedId);
    l.setBienId(Integer.parseInt(txtBienId.getText()));
    l.setDateDebut(dateDebut.getDate());
    l.setDateFin(dateFin.getDate());
    l.setLoyer(Double.parseDouble(txtLoyer.getText()));

    if (locationDao.update(l)) {
        JOptionPane.showMessageDialog(this, "Location modifiée !");
        loadLocations();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this, "Erreur modification !");
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage());
}

        
    }//GEN-LAST:event_bnUpdateActionPerformed

    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
        // TODO add your handling code here:
        
        if (selectedId == -1) {
    JOptionPane.showMessageDialog(this, "Sélectionnez une location !");
    return;
}

int confirm = JOptionPane.showConfirmDialog(
        this,
        "Voulez-vous supprimer cette location ?",
        "Confirmation",
        JOptionPane.YES_NO_OPTION
);

if (confirm == JOptionPane.YES_OPTION) {
    if (locationDao.delete(selectedId)) {
        JOptionPane.showMessageDialog(this, "Location supprimée !");
        loadLocations();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this, "Erreur suppression !");
    }
}
        
        
        
    }//GEN-LAST:event_bnDeleteActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
      
int row = jTable1.getSelectedRow();

if (row != -1) {
    selectedId = Integer.parseInt(model.getValueAt(row, 0).toString());
    txtBienId.setText(model.getValueAt(row, 1).toString());

    dateDebut.setDate((Date) model.getValueAt(row, 2));
    dateFin.setDate((Date) model.getValueAt(row, 3));

    txtLoyer.setText(model.getValueAt(row, 4).toString());
}
    

        
        
    }//GEN-LAST:event_jTable1MouseClicked

private void loadLocations() {
    model.setRowCount(0);

    List<Location> list = locationDao.findAll();

    for (Location l : list) {
        model.addRow(new Object[]{
            l.getId(),
            l.getBienId(),
            l.getDateDebut(),
            l.getDateFin(),
            l.getLoyer()
        });
    }
}

private void clearForm() {
    txtBienId.setText("");
    txtLoyer.setText("");
    dateDebut.setDate(null);
    dateFin.setDate(null);
    selectedId = -1;
}


