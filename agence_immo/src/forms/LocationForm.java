    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed
        // TODO add your handling code here:
   

    try {

        if (txtBienId.getText().isEmpty()
                || dateDebut.getDate() == null
                || dateFin.getDate() == null
                || txtLoyer.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires !");
            return;
        }

        Location l = new Location();
        l.setBienId(Integer.parseInt(txtBienId.getText()));
        l.setDateDebut(dateDebut.getDate());
        l.setDateFin(dateFin.getDate());
        l.setLoyer(Double.parseDouble(txtLoyer.getText()));

        if (locationDao.create(l)) {
            JOptionPane.showMessageDialog(this, "Location ajoutée !");
            loadLocations();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Erreur d'ajout !");
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage());
    }

        
        
    }//GEN-LAST:event_bnAddActionPerformed

